// Use dynamic build hash for cache versioning (changes with each deployment)
// This ensures old browsers automatically get new cache on rebuild
// Build hash is injected by scripts/inject-build-hash.js during build process
const BUILD_HASH = 'BUILD_HASH_PLACEHOLDER';
const CACHE_NAME = `randevubu-${BUILD_HASH}`;
const API_CACHE_NAME = `randevubu-api-${BUILD_HASH}`;

// Cache metadata for age tracking (cache busting strategy)
const CACHE_METADATA_KEY = 'randevubu-cache-metadata';
const MAX_CACHE_AGE_MS = 24 * 60 * 60 * 1000; // 24 hours

// Workbox will inject the manifest here
// This is required when using InjectManifest mode
const manifest = self.__WB_MANIFEST || [];

// ============================================
// CACHE METADATA & AGE TRACKING HELPERS
// ============================================

/**
 * Store cache timestamp for age tracking
 * Per project rules: cache busting strategy for stale content prevention
 */
function updateCacheMetadata(url) {
  self.clients.matchAll({ type: 'window' }).then(clients => {
    clients.forEach(client => {
      client.postMessage({
        type: 'CACHE_UPDATE',
        url: url,
        timestamp: Date.now()
      });
    });
  }).catch(() => {
    // Silently fail - metadata tracking is not critical
  });
}

/**
 * Check if cached response is too old
 * @returns true if cache is older than MAX_CACHE_AGE_MS
 */
function isCacheStale(cacheDate) {
  if (!cacheDate) return true;
  const age = Date.now() - parseInt(cacheDate);
  return age > MAX_CACHE_AGE_MS;
}

// Install event - cache essential assets with proper error handling
// Per project rules: separate concerns (critical vs optional), proper error handling
self.addEventListener('install', (event) => {
  log('[Service Worker] Installing...');
  self.skipWaiting();

  event.waitUntil(
    caches.open(CACHE_NAME).then((cache) => {
      log('[Service Worker] Caching essential assets');
      
      // Critical assets that MUST cache successfully
      // If these fail, installation should fail
      const criticalAssets = [
        '/',
        '/offline.html'
      ];
      
      // Optional assets (generated by Workbox)
      // Can fail without blocking installation
      const manifestUrls = manifest.map(entry => entry.url || entry);
      
      // First: cache critical assets (must succeed)
      return Promise.all(
        criticalAssets.map(url =>
          cache.add(url).catch(err => {
            error('[Service Worker] Failed to cache critical asset:', url, err);
            throw err; // Fail installation if critical asset fails
          })
        )
      ).then(() => {
        // Then: cache optional assets (failures don't block)
        return Promise.allSettled(
          manifestUrls.map(url =>
            cache.add(url).catch(err => {
              warn('[Service Worker] Failed to cache optional asset:', url, err);
            })
          )
        );
      });
    })
  );
});

// Activate event - clean up old caches
self.addEventListener('activate', (event) => {
  log('[Service Worker] Activating...');
  event.waitUntil(
    caches.keys().then((cacheNames) => {
      return Promise.all(
        cacheNames.map((cacheName) => {
          if (cacheName !== CACHE_NAME && cacheName !== API_CACHE_NAME) {
            log('[Service Worker] Deleting old cache:', cacheName);
            return caches.delete(cacheName);
          }
        })
      );
    }).then(() => {
      return self.clients.claim();
    })
  );
});

// Fetch event - implements stale-while-revalidate pattern
// Per project rules: efficient caching with background updates
// Returns cache immediately, refreshes in background
self.addEventListener('fetch', (event) => {
  const { request } = event;
  const url = new URL(request.url);

  // Skip non-http(s) requests
  if (!url.protocol.startsWith('http')) {
    return;
  }

  // API requests - network first with cache fallback
  if (url.pathname.startsWith('/api/')) {
    const isDev = self.location && (self.location.hostname === 'localhost' || self.location.hostname === '127.0.0.1');
    if (isDev) {
      // Development: always network, never cache
      event.respondWith(fetch(request));
      return;
    }
    
    // Production API: network first with cache fallback
    event.respondWith(
      fetch(request)
        .then(response => {
          // Cache successful GET responses
          if (response.ok && request.method === 'GET') {
            const responseClone = response.clone();
            caches.open(API_CACHE_NAME).then(cache => {
              cache.put(request, responseClone);
            });
          }
          return response;
        })
        .catch(() => {
          // Network failed - use cache for GET requests
          if (request.method === 'GET') {
            return caches.match(request) || 
              new Response(JSON.stringify({ error: 'Network error' }), {
                status: 503,
                headers: { 'Content-Type': 'application/json' }
              });
          }
          // Non-GET requests can't be cached
          return new Response(JSON.stringify({ error: 'Network error' }), {
            status: 503,
            headers: { 'Content-Type': 'application/json' }
          });
        })
    );
    return;
  }

  // Static assets - stale-while-revalidate pattern with cache age checking
  // Returns cache immediately, refreshes in background
  // Per project rules: cache busting strategy for preventing stale content
  event.respondWith(
    caches.match(request).then(response => {
      // Return cache if available (don't wait for network)
      if (response) {
        const cacheDate = response.headers.get('sw-cache-time');
        const isStale = isCacheStale(cacheDate);
        
        // Always try to refresh in background if network available
        // Stale or fresh, always keep cache updated
        fetch(request).then(networkResponse => {
          if (networkResponse && networkResponse.ok && request.method === 'GET') {
            const responseToCache = networkResponse.clone();
            caches.open(CACHE_NAME).then(cache => {
              // Add metadata about when this was cached
              const clonedResponse = new Response(responseToCache.body, {
                status: responseToCache.status,
                statusText: responseToCache.statusText,
                headers: new Headers(responseToCache.headers)
              });
              clonedResponse.headers.set('sw-cache-time', Date.now().toString());
              cache.put(request, clonedResponse);
              updateCacheMetadata(request.url);
            });
          }
        }).catch(() => {
          // Network request failed silently - cache is good enough
        });
        
        return response;
      }
      
      // No cache - wait for network
      return fetch(request)
        .then(response => {
          // Cache successful GET responses
          if (response.ok && request.method === 'GET') {
            const responseClone = response.clone();
            caches.open(CACHE_NAME).then(cache => {
              // Add metadata about when this was cached
              const clonedResponse = new Response(responseClone.body, {
                status: responseClone.status,
                statusText: responseClone.statusText,
                headers: new Headers(responseClone.headers)
              });
              clonedResponse.headers.set('sw-cache-time', Date.now().toString());
              cache.put(request, clonedResponse);
              updateCacheMetadata(request.url);
            });
          }
          return response;
        })
        .catch(() => {
          // Network failed and no cache available
          if (request.mode === 'navigate') {
            // For navigation requests, show offline page
            return caches.match('/offline.html');
          }
          // For other requests, return 404
          return new Response('Not found', { status: 404 });
        });
    })
  );
});

