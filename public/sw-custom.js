// Push event - receive and display notification
self.addEventListener('push', (event) => {
  log('[Service Worker] Push notification received:', event);

  let notificationData = {
    title: 'RandevuBu',
    body: 'Yeni bir bildiriminiz var',
    icon: '/icon-192.svg',
    badge: '/icon-192.svg',
    data: {}
  };

  if (event.data) {
    try {
      const payload = event.data.json();
      log('[Service Worker] Push payload:', payload);

      notificationData = {
        title: payload.title || notificationData.title,
        body: payload.body || payload.message || notificationData.body,
        icon: payload.icon || notificationData.icon,
        badge: payload.badge || notificationData.badge,
        tag: payload.tag || 'randevubu-notification',
        data: payload.data || {},
        requireInteraction: payload.requireInteraction || false,
        vibrate: [200, 100, 200],
        actions: payload.actions || [
          { action: 'view', title: 'Görüntüle' },
          { action: 'dismiss', title: 'Kapat' }
        ]
      };
    } catch (error) {
      error('[Service Worker] Failed to parse push payload:', error);
      notificationData.body = event.data.text();
    }
  }

  const promiseChain = self.registration.showNotification(
    notificationData.title,
    {
      body: notificationData.body,
      icon: notificationData.icon,
      badge: notificationData.badge,
      tag: notificationData.tag,
      data: notificationData.data,
      requireInteraction: notificationData.requireInteraction,
      vibrate: notificationData.vibrate,
      actions: notificationData.actions
    }
  );

  event.waitUntil(promiseChain);
});

// Notification click event
self.addEventListener('notificationclick', (event) => {
  log('[Service Worker] Notification clicked:', event);

  event.notification.close();

  const urlToOpen = event.notification.data?.url || '/dashboard';

  // Handle action buttons
  if (event.action) {
    log('[Service Worker] Notification action clicked:', event.action);

    switch (event.action) {
      case 'view':
        event.waitUntil(
          clients.openWindow(event.notification.data?.url || '/dashboard/appointments')
        );
        return;
      case 'reschedule':
        event.waitUntil(
          clients.openWindow(event.notification.data?.rescheduleUrl || '/dashboard/appointments')
        );
        return;
      case 'accept':
        event.waitUntil(
          clients.openWindow(event.notification.data?.acceptUrl || '/dashboard/appointments')
        );
        return;
      case 'dismiss':
        return;
      default:
        break;
    }
  }

  // Default click behavior - focus existing window or open new one
  event.waitUntil(
    clients.matchAll({ type: 'window', includeUncontrolled: true }).then((clientList) => {
      // Check if there's already a window open
      for (const client of clientList) {
        if (client.url.includes(urlToOpen) && 'focus' in client) {
          return client.focus();
        }
      }

      // If no window is open, open a new one
      if (clients.openWindow) {
        return clients.openWindow(urlToOpen);
      }
    })
  );
});

// Notification close event
self.addEventListener('notificationclose', (event) => {
  log('[Service Worker] Notification closed:', event);

  // Track notification dismissal if needed
  if (event.notification.data?.trackDismissal) {
    event.waitUntil(
      fetch('/api/v1/push-notifications/track', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          notificationId: event.notification.data.id,
          action: 'dismissed'
        })
      }).catch(err => console.error('[Service Worker] Failed to track dismissal:', err))
    );
  }
});

// Push subscription change event
self.addEventListener('pushsubscriptionchange', (event) => {
  log('[Service Worker] Push subscription changed');

  event.waitUntil(
    self.registration.pushManager.subscribe({
      userVisibleOnly: true,
      applicationServerKey: event.oldSubscription?.options?.applicationServerKey
    }).then(subscription => {
      log('[Service Worker] Re-subscribed:', subscription);

      // Send new subscription to server
      return fetch('/api/v1/push-notifications/subscribe', {
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
        },
        body: JSON.stringify({
          endpoint: subscription.endpoint,
          keys: {
            p256dh: btoa(String.fromCharCode(...new Uint8Array(subscription.getKey('p256dh')))),
            auth: btoa(String.fromCharCode(...new Uint8Array(subscription.getKey('auth'))))
          }
        })
      });
    }).catch(err => {
      error('[Service Worker] Failed to re-subscribe:', err);
    })
  );
});

